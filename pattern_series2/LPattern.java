package pattern_series2;

public class LPattern {
    public static void main(String[] args) {
        // for column
        for(int i=0;i<6;i++)
        {
            System.out.println("*");
        }
        // for row
        for(int i=0;i<5;i++)
        {
            System.out.print("*"+" ");
        }
    }
}
