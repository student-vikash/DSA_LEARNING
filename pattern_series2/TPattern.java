package pattern_series2;

public class TPattern {
    public static void main(String[] args) {
        // for row
        for(int i=0;i<7;i++)
        {
            System.out.print("*"+" ");
        }

        // for column
        for(int i=0;i<6;i++)
        {
            System.out.println("      *");
        }
    }
}
